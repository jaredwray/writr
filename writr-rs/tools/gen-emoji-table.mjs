// Generates crates/writr-core/src/generated/emoji_table.rs from
// node-emoji@2.2.0 (the exact dataset remark-emoji@5.0.2 resolves against —
// note: node-emoji/emojilib, NOT gemoji).
//
// Run from the repo root: node writr-rs/tools/gen-emoji-table.mjs
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { importPinned } from "./pinned.mjs";

const OUT = path.join(
	path.dirname(fileURLToPath(import.meta.url)),
	"..",
	"crates",
	"writr-core",
	"src",
	"generated",
	"emoji_table.rs",
);

const emojilib = (await importPinned("emojilib", "2.4.0")).default;
const nodeEmoji = await importPinned("node-emoji", "2.2.0");

// Mirror node-emoji's data view: Object.entries(emojilib.lib) → [name, char].
const entries = Object.entries(emojilib.lib).map(([name, value]) => [
	name,
	value.char,
]);

// Cross-check against node-emoji's own lookups, then sort for binary search.
for (const [name, char] of entries) {
	const got = nodeEmoji.get(name);
	if (got !== char) {
		throw new Error(`node-emoji.get(${name}) = ${got}, expected ${char}`);
	}
}
entries.sort(([a], [b]) => (a < b ? -1 : a > b ? 1 : 0));

const escape = (s) =>
	s.replace(/\\/g, "\\\\").replace(/"/g, '\\"').replace(/\n/g, "\\n");

const body = `// @generated by writr-rs/tools/gen-emoji-table.mjs — do not edit.
//
// Source: node-emoji@2.2.0 / emojilib@2.4.0 (the dataset behind
// remark-emoji@5.0.2). \`(name, emoji)\`, sorted by name (byte order).

pub static EMOJI_BY_NAME: &[(&str, &str)] = &[
${entries.map(([name, char]) => `\t("${escape(name)}", "${escape(char)}"),`).join("\n")}
];
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, body);
console.log(`wrote ${OUT} (${entries.length} entries)`);
