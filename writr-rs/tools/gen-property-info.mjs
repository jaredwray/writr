// Generates crates/writr-core/src/generated/property_info_data.rs from the
// exact property-information version pinned in the repo's pnpm lockfile.
//
// Run from the repo root:
//   node writr-rs/tools/gen-property-info.mjs
//
// The output is committed. CI re-runs this script and fails if the output
// drifts, so the table always matches the JS package the goldens were
// generated with.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { importPinned } from "./pinned.mjs";

const { html, svg } = await importPinned("property-information", "7.1.0");

const OUT = path.join(
	path.dirname(fileURLToPath(import.meta.url)),
	"..",
	"crates",
	"writr-core",
	"src",
	"generated",
	"property_info_data.rs",
);

const FLAGS = {
	boolean: 1,
	booleanish: 2,
	overloadedBoolean: 4,
	number: 8,
	spaceSeparated: 16,
	commaSeparated: 32,
	commaOrSpaceSeparated: 64,
	mustUseProperty: 128,
};

function escape(s) {
	return s.replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function dumpSchema(name, schema) {
	// property name -> (attribute, flags), sorted for binary search.
	const props = Object.keys(schema.property)
		.sort()
		.map((property) => {
			const info = schema.property[property];
			let flags = 0;
			for (const [key, bit] of Object.entries(FLAGS)) {
				if (info[key]) flags |= bit;
			}
			return `\t("${escape(property)}", "${escape(info.attribute)}", ${flags}),`;
		});
	// normalized (lowercased) name -> property name, sorted for binary search.
	const normal = Object.keys(schema.normal)
		.sort()
		.map(
			(normalized) =>
				`\t("${escape(normalized)}", "${escape(schema.normal[normalized])}"),`,
		);
	return [
		`/// \`(property, attribute, flags)\` for the ${name} space, sorted by property.`,
		`pub static ${name.toUpperCase()}_PROPERTIES: &[(&str, &str, u8)] = &[`,
		...props,
		`];`,
		``,
		`/// \`(normalized name, property)\` for the ${name} space, sorted by normalized name.`,
		`pub static ${name.toUpperCase()}_NORMAL: &[(&str, &str)] = &[`,
		...normal,
		`];`,
	].join("\n");
}

const header = `// @generated by writr-rs/tools/gen-property-info.mjs — do not edit.
//
// Source: property-information@7.1.0 (the version pinned in pnpm-lock.yaml at
// generation time). Flags bitmask: 1=boolean, 2=booleanish, 4=overloadedBoolean,
// 8=number, 16=spaceSeparated, 32=commaSeparated, 64=commaOrSpaceSeparated,
// 128=mustUseProperty.
`;

const body = `${header}
${dumpSchema("html", html)}

${dumpSchema("svg", svg)}
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, body);
console.log(`wrote ${OUT}`);
