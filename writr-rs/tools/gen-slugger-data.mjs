// Generates crates/writr-core/src/generated/slug_ranges.rs from
// github-slugger@2.0.0 by evaluating its generated strip-regex against every
// Unicode scalar value. This captures the exact per-codepoint behavior of
// `value.replace(regex, '')` without transcribing the regex.
//
// Run from the repo root: node writr-rs/tools/gen-slugger-data.mjs
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { importPinned } from "./pinned.mjs";

const OUT = path.join(
	path.dirname(fileURLToPath(import.meta.url)),
	"..",
	"crates",
	"writr-core",
	"src",
	"generated",
	"slug_ranges.rs",
);

const { regex } = await importPinned("github-slugger", "2.0.0", "regex.js");

// Space is handled separately by the slugger (` ` → `-`); every other
// codepoint is either kept or removed. Verify removal is all-or-nothing per
// codepoint (a partial removal would leave lone surrogates — impossible to
// represent in Rust, and would indicate the regex matches individual
// surrogate halves).
const removedRanges = [];
let rangeStart = -1;
let previous = -1;
for (let cp = 0; cp <= 0x10ffff; cp++) {
	if (cp >= 0xd800 && cp <= 0xdfff) continue; // surrogates: not scalar values
	const char = String.fromCodePoint(cp);
	const result = char.replace(regex, "");
	if (result !== "" && result !== char) {
		throw new Error(
			`partial removal at U+${cp.toString(16)} — regex matches surrogate halves`,
		);
	}
	const removed = result === "";
	if (removed) {
		if (rangeStart === -1) {
			rangeStart = cp;
		} else if (cp !== previous + 1) {
			removedRanges.push([rangeStart, previous]);
			rangeStart = cp;
		}
		previous = cp;
	}
}
if (rangeStart !== -1) {
	removedRanges.push([rangeStart, previous]);
}

// Sanity checks against the real slugger.
const slugger = await importPinned("github-slugger", "2.0.0");
const cases = [
	["Hello World", "hello-world"],
	["Café Über Ünïcode", "café-über-ünïcode"],
	["Hello, World! (Punctuation)", "hello-world-punctuation"],
	["Launch :rocket:", "launch-rocket"],
	["Launch 🚀", "launch-"],
];
for (const [input, expected] of cases) {
	const got = slugger.slug(input);
	if (got !== expected) {
		throw new Error(`slug(${JSON.stringify(input)}) = ${got} != ${expected}`);
	}
}

const hex = (cp) => `0x${cp.toString(16).toUpperCase()}`;

const body = `// @generated by writr-rs/tools/gen-slugger-data.mjs — do not edit.
//
// Source: github-slugger@2.0.0. Inclusive codepoint ranges REMOVED by the
// slugger's strip regex (space U+20 is later converted to \`-\`), sorted for
// binary search.

pub static REMOVED_RANGES: &[(u32, u32)] = &[
${removedRanges.map(([a, b]) => `\t(${hex(a)}, ${hex(b)}),`).join("\n")}
];
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, body);
console.log(`wrote ${OUT} (${removedRanges.length} ranges)`);
