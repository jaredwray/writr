// Generates crates/writr-core/src/generated/unicode_data.rs from
// micromark-util-character (the checks mdast-util-gfm-autolink-literal's
// `previous()` gate uses). Evaluated per UTF-16 unit — exactly how the JS
// runs them via `charCodeAt`.
//
// Run from the repo root: node writr-rs/tools/gen-unicode-data.mjs
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { importPinned } from "./pinned.mjs";

const OUT = path.join(
	path.dirname(fileURLToPath(import.meta.url)),
	"..",
	"crates",
	"writr-core",
	"src",
	"generated",
	"unicode_data.rs",
);

const { unicodePunctuation, unicodeWhitespace } = await importPinned(
	"micromark-util-character",
	"2.1.1",
);

function ranges(test) {
	const out = [];
	let start = -1;
	let previous = -1;
	for (let unit = 0; unit <= 0xffff; unit++) {
		if (test(unit)) {
			if (start === -1) {
				start = unit;
			} else if (unit !== previous + 1) {
				out.push([start, previous]);
				start = unit;
			}
			previous = unit;
		}
	}
	if (start !== -1) out.push([start, previous]);
	return out;
}

const hex = (n) => `0x${n.toString(16).toUpperCase()}`;
const table = (name, list) =>
	`pub static ${name}: &[(u16, u16)] = &[\n${list
		.map(([a, b]) => `\t(${hex(a)}, ${hex(b)}),`)
		.join("\n")}\n];`;

const punct = ranges((unit) => unicodePunctuation(unit));
const white = ranges((unit) => unicodeWhitespace(unit));

const body = `// @generated by writr-rs/tools/gen-unicode-data.rs — do not edit.
//
// Source: micromark-util-character@2.1.1. Inclusive UTF-16 unit ranges for
// \`unicodePunctuation\` (\\p{P}|\\p{S}) and \`unicodeWhitespace\` (\\s), sorted
// for binary search.

${table("UNICODE_PUNCTUATION_RANGES", punct)}

${table("UNICODE_WHITESPACE_RANGES", white)}
`;

fs.mkdirSync(path.dirname(OUT), { recursive: true });
fs.writeFileSync(OUT, body);
console.log(
	`wrote ${OUT} (${punct.length} punctuation ranges, ${white.length} whitespace ranges)`,
);
